    private void btn_senf_requestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_senf_requestActionPerformed
        int index_row = table_result.getSelectedRow();
        if(index_row == -1){
            JOptionPane.showMessageDialog(null, "Chọn một tài khoảng người dùng");
        }else{
            int confirm = JOptionPane.showConfirmDialog(null, "Kết Bạn với: "+this.arr.get(index_row)[1]+" (ID: "+ this.arr.get(index_row)[0] + ")");
            switch (confirm) {
                case JOptionPane.YES_OPTION:
                    System.out.println("yes "+this.arr.get(index_row)[0]);
                    data_socket dtsk = new data_socket();
                    dtsk.action = "add_relation";
                    String[] data = new String[2];
                    data[0] = this.arr.get(index_row)[0];
                    data[1] = "2";
                    dtsk.data = data;
                    
                    {
                        try {
                            ObjectOutputStream dout = new ObjectOutputStream(Chat_client.socket.getOutputStream());
                            dout.writeObject(dtsk);
                        } catch (IOException ex) {
                            Logger.getLogger(fr_result_search.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    break;
                case JOptionPane.NO_OPTION:
                    System.out.println("no "+this.arr.get(index_row)[0]);
                    break;
                case JOptionPane.CANCEL_OPTION:
                    System.out.println("cancel "+this.arr.get(index_row)[0]);
                    break;
                default:
                    break;
            }
        }
    }//GEN-LAST:event_btn_senf_requestActionPerformed

